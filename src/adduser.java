    public void add_user() throws IOException{
        us=f.getUse();
        int check=0;
        String name=jTFiduser.getText();
        for(int i=0;i<us.size();i++){
            if(us.get(i).getName().equals(name)||us.get(i).getName().equalsIgnoreCase("admin")){
                check++;break;
            }
        }
        if(check==0){
            if(jPasswordField1.getText().equals(jPasswordField2.getText())){
                String pass=jPasswordField1.getText();
                f.add_user(name, pass);
                JOptionPane.showMessageDialog(this,"User added successfully");
                reset();
            }
            else{
                //password not same
                JOptionPane.showMessageDialog(this,"Enter same password");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Username already exists!");
            reset();
        }
    }
    public void reset(){
        jTFiduser.setText("");
        jPasswordField1.setText("");
        jPasswordField2.setText("");
    }
    

        
